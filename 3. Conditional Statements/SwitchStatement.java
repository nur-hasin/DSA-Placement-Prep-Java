public class SwitchStatement {
    public static void main(String[] args) {
        int num = 3;

        switch (num) {
            case 1:
                System.out.println("Noodles");
                break;
            case 2:
                System.out.println("Burger");
                break;
            case 3:
                System.out.println("Chocolate Shake");
                break;
            default:
                System.out.println("Realise these are dreams");   
        } 
    }
} 